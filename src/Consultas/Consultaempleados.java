    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("EmpleadoID");
            modelo.addColumn("Nombrecompleto");
            modelo.addColumn("Edad");
            modelo.addColumn("Fechanacimiento");
            modelo.addColumn("telefono");
            modelo.addColumn("cedula");
            modelo.addColumn("horariotrabajo");
            modelo.addColumn("cargo");
            modelo.addColumn("tiposeguro");
            modelo.addColumn("tiposueldo");
            modelo.addColumn("sueldo");
            modelo.addColumn("fechaingreso");
            modelo.addColumn("estado");

        String filtro = txtbuscarempleados.getText().trim();

        try {
            String query = "SELECT * FROM empleados where EmpleadoID like ? or Nombrecompleto like ? ";
            PreparedStatement ps = cn.prepareStatement(query);
            ps.setString(1, "%" + filtro + "%");
            ps.setString(2, "%" + filtro + "%");

            ResultSet rs = ps.executeQuery();

            modelo.setRowCount(0);

            while (rs.next()) {
                Object[] fila = new Object[13];

                fila[0] = rs.getInt("EmpleadoID");
                fila[1] = rs.getString("Nombrecompleto");
                fila[2] = rs.getString("edad");
                fila[3] = rs.getString("fechanacimiento");
                fila[4] = rs.getString("telefono");
                fila[5] = rs.getString("cedula");
                fila[6] = rs.getString("horariotrabajo");
                fila[7] = rs.getString("cargo");
                fila[8] = rs.getString("tiposeguro");
                fila[9] = rs.getString("tiposueldo");
                fila[10] = rs.getString("sueldo");
                fila[11] = rs.getString("fechaingreso");
                fila[12] = rs.getString("estado");
                

                modelo.addRow(fila);
            }

            jTable1.setModel(modelo);

            ps.close();
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al obtener los registros: " + e.getMessage());
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

