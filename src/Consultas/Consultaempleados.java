    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
  Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        String query = "SELECT * FROM empleados";
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("EmpleadoID");
        model.addColumn("Nombre Completo");
        model.addColumn("Edad");
        model.addColumn("Fecha Nacimiento");
        model.addColumn("Teléfono");
        model.addColumn("Cédula");
        model.addColumn("Horario de Trabajo");
        model.addColumn("Cargo");
        model.addColumn("Tipo de Seguro");
        model.addColumn("Tipo de Sueldo");
        model.addColumn("Sueldo");
        model.addColumn("Fecha Ingreso");
        model.addColumn("Estado");

        try (PreparedStatement ps = cn.prepareStatement(query); ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("EmpleadoID"),
                    rs.getString("nombrecompleto"),
                    rs.getString("edad"),
                    rs.getString("fechanacimiento"),
                    rs.getString("telefono"),
                    rs.getString("cedula"),
                    rs.getString("horariotrabajo"),
                    rs.getString("cargo"),
                    rs.getString("tiposeguro"),
                    rs.getString("tiposueldo"),
                    rs.getString("sueldo"),
                    rs.getString("fechaingreso"),
                    rs.getString("estado")
                });
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al mostrar los empleados: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }                                          

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {                                              
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();
        
        ResultSet rs;
        PreparedStatement ps;
   
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTable1.setModel(modelo);
            
            ps=cn.prepareStatement("SELECT * FROM EMPLEADOS");
            rs=ps.executeQuery();
                        
            java.sql.ResultSetMetaData rsMd =rs.getMetaData();
            int cColumnas = rsMd.getColumnCount();
            modelo.addColumn("EmpleadoID");
            modelo.addColumn("Nombrecompleto");
            modelo.addColumn("Edad");
            modelo.addColumn("Fechanacimiento");
            modelo.addColumn("telefono");
            modelo.addColumn("cedula");
            modelo.addColumn("horariotrabajo");
            modelo.addColumn("cargo");
            modelo.addColumn("tiposeguro");
            modelo.addColumn("tiposueldo");
            modelo.addColumn("sueldo");
            modelo.addColumn("fechaingreso");
            modelo.addColumn("estado");
            
            while (rs.next()){
                Object[] filas = new Object [cColumnas];
                for (int i=0; i<cColumnas;i++){
                    filas[i]=rs.getObject(i+1);
                }
                modelo.addRow(filas);
                    
            }
    
         }catch(SQLException e){
  
                JOptionPane.showMessageDialog(null, "No se pudieron mostrar los datos  " + e.getMessage());
   }
    }//GEN-LAST:event_btnmostrarActionPerformed

