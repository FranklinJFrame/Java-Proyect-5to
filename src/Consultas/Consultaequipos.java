    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("EquipoID");
        model.addColumn("Nombreequipo");
        model.addColumn("Numeroserie");
        model.addColumn("cantidad");
        model.addColumn("PesoKg");
        model.addColumn("utilidad");
        model.addColumn("descripcion");
        model.addColumn("Estado");

        String filtro = txtbuscarequipos.getText().trim();

        try {
            String query = "SELECT * FROM inventario_equipos where EquipoID like ? or Nombreequipo like ? ";
            PreparedStatement ps = cn.prepareStatement(query);
            ps.setString(1, "%" + filtro + "%");
            ps.setString(2, "%" + filtro + "%");

            ResultSet rs = ps.executeQuery();

            model.setRowCount(0);

            while (rs.next()) {
                Object[] fila = new Object[13];

                fila[0] = rs.getInt("EquipoID");
                fila[1] = rs.getString("Nombreequipo");
                fila[2] = rs.getString("Numeroserie");
                fila[3] = rs.getString("cantidad");
                fila[4] = rs.getString("PesoKg");
                fila[5] = rs.getString("Utilidad");
                fila[6] = rs.getString("Descripcion");
                fila[7] = rs.getString("Estado");

                model.addRow(fila);
            }

            jTable1.setModel(model);

            ps.close();
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al obtener los registros: " + e.getMessage());
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

