    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        String query = "SELECT * FROM inventario_equipos";
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("EquipoID");
        model.addColumn("Nombreequipo");
        model.addColumn("Numeroserie");
        model.addColumn("cantidad");
        model.addColumn("PesoKg");
        model.addColumn("utilidad");
        model.addColumn("descripcion");
        model.addColumn("Estado");
        

        try (PreparedStatement ps = cn.prepareStatement(query); ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("EquipoID"),
                    rs.getString("nombreequipo"),
                    rs.getString("numeroserie"),
                    rs.getString("cantidad"),
                    rs.getString("pesoKg"),
                    rs.getString("utilidad"),
                    rs.getString("descripcion"),
                    rs.getString("estado"),
                });
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al mostrar los empleados: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
   
    }//GEN-LAST:event_btnmostrarActionPerformed

