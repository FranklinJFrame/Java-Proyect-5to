    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed

        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        ResultSet rs;
        PreparedStatement ps;

        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTable1.setModel(modelo);

            ps=cn.prepareStatement("SELECT * FROM clientes");
            rs=ps.executeQuery();

            java.sql.ResultSetMetaData rsMd =rs.getMetaData();
            int cColumnas = rsMd.getColumnCount();
            modelo.addColumn("clienteID");
            modelo.addColumn("Nombre");
            modelo.addColumn("apellido");
            modelo.addColumn("Telefono");
            modelo.addColumn("domicilio");
            modelo.addColumn("numerodecasa");
            modelo.addColumn("codigopostal");
            modelo.addColumn("estado");

            while (rs.next()){
                Object[] filas = new Object [cColumnas];
                for (int i=0; i<cColumnas;i++){
                    filas[i]=rs.getObject(i+1);
                }
                modelo.addRow(filas);

            }

        }catch(SQLException e){

            JOptionPane.showMessageDialog(null, "No se pudieron mostrar los datos  " + e.getMessage());
        }
    }//GEN-LAST:event_btnmostrarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("clienteID");
        modelo.addColumn("Nombre");
        modelo.addColumn("apellido");
        modelo.addColumn("Telefono");
        modelo.addColumn("domicilio");
        modelo.addColumn("numerodecasa");
        modelo.addColumn("codigopostal");
        modelo.addColumn("estado");

        String filtro = txtbuscarpaquetes.getText().trim();

        try {
            String query = "SELECT * FROM clientes where clienteID like ? or Nombre like ? ";
            PreparedStatement ps = cn.prepareStatement(query);
            ps.setString(1, "%" + filtro + "%");
            ps.setString(2, "%" + filtro + "%");

            ResultSet rs = ps.executeQuery();

            modelo.setRowCount(0);

            while (rs.next()) {
                Object[] fila = new Object[8];

                fila[0] = rs.getString("clienteID");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getString("apellido");
                fila[3] = rs.getString("telefono");
                fila[4] = rs.getString("domicilio");
                fila[5] = rs.getString("numerodecasa");
                fila[6] = rs.getString("codigopostal");
                fila[7] = rs.getString("estado");

                modelo.addRow(fila);
            }

            jTable1.setModel(modelo);

            ps.close();
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al obtener los registros: " + e.getMessage());
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

