public class Conductores {
    private int conductorID;
    private int empleadoID;
    private String Nombre;
    private String tipoLicencia;
    private String vehiculosQuePuedeConducir;
    private String nivelExperiencia;

    public int getConductorID() {
        return conductorID;
    }

    public void setConductorID(int conductorID) {
        this.conductorID = conductorID;
    }

    public int getEmpleadoID() {
        return empleadoID;
    }

    public void setEmpleadoID(int empleadoID) {
        this.empleadoID = empleadoID;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getTipoLicencia() {
        return tipoLicencia;
    }

    public void setTipoLicencia(String tipoLicencia) {
        this.tipoLicencia = tipoLicencia;
    }

    public String getVehiculosQuePuedeConducir() {
        return vehiculosQuePuedeConducir;
    }

    public void setVehiculosQuePuedeConducir(String vehiculosQuePuedeConducir) {
        this.vehiculosQuePuedeConducir = vehiculosQuePuedeConducir;
    }

    public String getNivelExperiencia() {
        return nivelExperiencia;
    }

    public void setNivelExperiencia(String nivelExperiencia) {
        this.nivelExperiencia = nivelExperiencia;
    }

    

    
}
