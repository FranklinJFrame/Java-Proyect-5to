    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
    ResultSet rs;
    PreparedStatement ps;

    try {
        int ConductorID = Integer.parseInt(txtidconductor.getText());
        String query = "select * from conductores where ConductorID = ?";
        ps = cn.prepareStatement(query);
        ps.setInt(1, ConductorID);
        rs = ps.executeQuery();

        if (rs.next()) {
            txtidempleado.setText(String.valueOf(rs.getInt("EmpleadoID")));
            txtnombre.setText(rs.getString("Nombre"));
            txttipolicencia.setText(rs.getString("TipoLicencia"));
            txtvehiculo.setText(rs.getString("VehiculosQuePuedeConducir"));
            txtnivelexperiencia.setText(rs.getString("NivelExperiencia"));
            txtestado.setText(rs.getString("Estado"));
        } else {
            JOptionPane.showMessageDialog(null, "Conductor no encontrado.");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
    }
        
    }//GEN-LAST:event_btnbuscarActionPerformed

