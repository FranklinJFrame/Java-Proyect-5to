    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();
        
        int ConductorID = Integer.parseInt(txtidconductor.getText());
        int EmpleadoID = Integer.parseInt(txtidempleado.getText());
        String nombre = txtnombre.getText();
        String TipoLicencia = txttipolicencia.getText();
        String vehiculosQuePuedeConducir = txtvehiculo.getText();
        String NivelExperiencia =txtnivelexperiencia.getText();
        String Estado = txtestado.getText();
        
        String registro = "INSERT INTO conductores(ConductorID, EmpleadoID,nombre, TipoLicencia, VehiculosQuePuedeConducir, NivelExperiencia, EStado) VALUES(?, ?, ?, ?, ?,?,?)";
        try (PreparedStatement ps = cn.prepareStatement(registro)){
            ps.setInt(1, ConductorID);
            ps.setInt(2, EmpleadoID);
            ps.setString(3, nombre);
            ps.setString(4, TipoLicencia);
            ps.setString(5, vehiculosQuePuedeConducir);
            ps.setString(6, NivelExperiencia);
            ps.setString(7, Estado);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos guardados correctamente.");
            limpiarCampos();
        }
        catch (SQLException e){
                JOptionPane.showMessageDialog(null, "No se pudo guardar: " + e.getMessage());
                }
        
        
    }//GEN-LAST:event_btnregistrarActionPerformed

