    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();
        ResultSet rs;
        PreparedStatement ps;

        try {
            int pagoID = Integer.parseInt(txtidpago.getText());
            String query = "select * from pago where pagoID = ?";
            ps = cn.prepareStatement(query);
            ps.setInt(1, pagoID);
            rs = ps.executeQuery();

            if (rs.next()) {
                txtidpago.setText(String.valueOf(rs.getInt("pagoID")));
                txtmontototal.setText(rs.getString("montototal"));
                txtidcliente.setText(rs.getString("clienteId"));
                txtfechapago.setText(rs.getString("Fechapago"));
                txtidcotizacion.setText(rs.getString("cotizacionId"));
                txtestado.setText(rs.getString("Estado"));
            } else {
                JOptionPane.showMessageDialog(null, "Sucursal no encontrada.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

