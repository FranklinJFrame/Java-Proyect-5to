    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
     Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();
        ResultSet rs;
        PreparedStatement ps;

    try {
        int paqueteID = Integer.parseInt(txtidpaquete.getText());
        String query = "select * from inventario_paquetes where paqueteID = ?";
        ps = cn.prepareStatement(query);
        ps.setInt(1, paqueteID);
        rs = ps.executeQuery();

        if (rs.next()) {
            txtnumeroserial.setText(rs.getString("Numeroserial"));
            txtnombredelcliente.setText(rs.getString("Nombrecliente"));
            txtnombrereceptor.setText(rs.getString("Nombrereceptor"));
            txtfechaingreso.setText(rs.getString("FechaIngresoPaquete"));
            txtfechayhora.setText(rs.getString("FechaYhorapartida"));
            txtestado.setText(rs.getString("Estado"));
        } else {
            JOptionPane.showMessageDialog(null, "Paquete no encontrado.");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
    }
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        ResultSet rs;
        PreparedStatement ps;

        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTable1.setModel(modelo);

            ps=cn.prepareStatement("SELECT * FROM INVENTARIO_PAQUETES");
            rs=ps.executeQuery();

            java.sql.ResultSetMetaData rsMd =rs.getMetaData();
            int cColumnas = rsMd.getColumnCount();
            modelo.addColumn("paqueteID");
            modelo.addColumn("NumeroSerial");
            modelo.addColumn("NombreCliente");
            modelo.addColumn("NombreReceptor");
            modelo.addColumn("FechaIngresoPaquete");
            modelo.addColumn("FechaYHorapartida");
            modelo.addColumn("Estado");

            while (rs.next()){
                Object[] filas = new Object [cColumnas];
                for (int i=0; i<cColumnas;i++){
                    filas[i]=rs.getObject(i+1);
                }
                modelo.addRow(filas);

            }

        }catch(SQLException e){

            JOptionPane.showMessageDialog(null, "No se pudieron mostrar los datos  " + e.getMessage());
        }
    }//GEN-LAST:event_btnmostrarActionPerformed

