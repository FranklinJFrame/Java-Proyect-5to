    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();

        String clienteID = txtidcliente.getText();
        String Nombre = txtnombre.getText();
        String Apellido = txtapellido.getText();
        String Telefono = txttelefono.getText();
        String Domicilio = txtdomicilio.getText();
        String Numerodecasa = txtnumerodecasa.getText();
        String Codigopostal = txtcodigopostal.getText();
        String Estado = txtestado.getText();

        String query="Update clientes set  clienteId=?, Nombre=?, Apellido=?, Telefono=?,  Domicilio=?, Numerodecasa=?, Codigopostal=?, Estado=? where clienteId=? ";
        try(PreparedStatement ps=cn.prepareStatement(query))  {

            ps.setString(1, clienteID);
            ps.setString(2, Nombre);
            ps.setString(3, Apellido);
            ps.setString(4, Telefono);
            ps.setString(5, Domicilio);
            ps.setString(6, Numerodecasa);
            ps.setString(7, Codigopostal);
            ps.setString(8, Estado);
            ps.setString(9, clienteID);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "datos actualizados correctamente");

        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "no se pudo actualizar correctamente"+e.getMessage());
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

