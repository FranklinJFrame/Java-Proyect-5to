    private void btneliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminar1ActionPerformed
        Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();
        ResultSet rs;
        PreparedStatement ps;

        try {
           String clienteID = txtidcliente.getText();
            String query = "select * from clientes where clienteID = ?";
            ps = cn.prepareStatement(query);
            ps.setString(1, clienteID);
            rs = ps.executeQuery();

            if (rs.next()) {
                txtidcliente.setText(rs.getString("clienteID"));
                txtnombre.setText(rs.getString("Nombre"));
                txtapellido.setText(rs.getString("Apellido"));
                txttelefono.setText(rs.getString("Telefono"));
                txtdomicilio.setText(rs.getString("Domicilio"));
                txtnumerodecasa.setText(rs.getString("NumerodeCasa"));
                txtcodigopostal.setText(rs.getString("codigopostal"));
                txtestado.setText(rs.getString("Estado"));
            } else {
                JOptionPane.showMessageDialog(null, "Receptor no encontrado.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
        }

    }//GEN-LAST:event_btneliminar1ActionPerformed

