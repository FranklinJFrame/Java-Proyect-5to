    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
       Conexion mysql = new Conexion();
    java.sql.Connection cn = mysql.Conectar();
        String idUsuario = txtidusuario.getText();
        String nombre = txtnombre.getText();
        String nombredeUsuario = txtnombreusuario.getText();
        String apellido = txtapellido.getText();
         String contraseña = new String(txtcontraseña.getPassword());
        String confirmarContraseña = new String(txtconfirmarcontraseña.getPassword());
        String estado = txtestado.getText();
        
        if (!contraseña.equals(confirmarContraseña)) {
            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.", "Error de contraseña", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String query = "INSERT INTO usuarios (idusuario, nombre, nombredeusuario, apellido, estado, clave) VALUES (?, ?, ?, ?, ?, ?)";

      try (PreparedStatement ps = cn.prepareStatement(query)) {
            ps.setString(1, idUsuario);
            ps.setString(2, nombre);
            ps.setString(3, nombredeUsuario);
            ps.setString(4, apellido);
            ps.setString(5, estado);
            ps.setString(6, contraseña);

            int result = ps.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Usuario registrado exitosamente.");
            } else {
                JOptionPane.showMessageDialog(null, "Error al registrar el usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al registrar el usuario: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

