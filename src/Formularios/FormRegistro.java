    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
    Conexion mysql = new Conexion();
        java.sql.Connection cn = mysql.Conectar();
        ResultSet rs;
        PreparedStatement ps;

    try {
        int idusuario = Integer.parseInt(txtidusuario.getText());
        String query = "select * from usuarios where idusuario = ?";
        ps = cn.prepareStatement(query);
        ps.setInt(1, idusuario);
        rs = ps.executeQuery();

        if (rs.next()) {
            txtidusuario.setText(String.valueOf(rs.getInt("idusuario")));
            txtnombre.setText(rs.getString("Nombre"));
            txtapellido.setText(rs.getString("Apellido"));
            txtnombreusuario.setText(rs.getString("nombredeusuario"));
            txtcontraseña.setText(rs.getString("clave"));
            txtestado.setText(rs.getString("Estado"));
        } else {
            JOptionPane.showMessageDialog(null, "usuario no encontrado.");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
    }
        
    }//GEN-LAST:event_btnbuscarActionPerformed

